const KEY = "budget-data";

export const getData = () => {
  const data = localStorage.getItem(KEY);
  return data ? JSON.parse(data) : [];
};

export const saveData = (transactions) => {
  localStorage.setItem(KEY, JSON.stringify(transactions));
};
